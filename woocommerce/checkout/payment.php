		<?php //echo apply_filters( 'woocommerce_order_button_html', '<input type="submit" class="button alt" name="woocommerce_checkout_place_order" id="place_order" value="' . esc_attr( $order_button_text ) . '" data-value="' . esc_attr( $order_button_text ) . '" />' ); ?>

		<h3 class="title"><?php _e( 'Purchase Order Number', 'woocommerce' ); ?></h3>
        <p class="form-row form-row-wide validate-required"><label><?php _e('Please enter the purchase order number here <abbr title="required" class="required">*</abbr>', 'shorts');?></label><input type="text" name="po_number" class="input-text" placeholder="<?php _e('Purchase Order Number', 'shorts');?>"></p>
        <p>&nbsp;</p>

		<?php if ( wc_get_page_id( 'terms' ) > 0 && apply_filters( 'woocommerce_checkout_show_terms', true ) ) : ?>
			<p class="form-row terms">
				<label for="terms" class="checkbox"><?php printf( __( 'I&rsquo;ve read and accept the <a href="%s" target="_blank">delivery, returns & warranty policy</a>', 'woocommerce' ), esc_url( wc_get_page_permalink( 'terms' ) ) ); ?></label>
				<input type="checkbox" class="input-checkbox" name="terms" <?php checked( apply_filters( 'woocommerce_terms_is_checked_default', isset( $_POST['terms'] ) ), true ); ?> id="terms" />
			</p>
		<?php endif; ?>

		<?php do_action( 'woocommerce_review_order_after_submit' ); ?>

	</div>

